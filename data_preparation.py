import pandas as pd
import numpy as np

# 1. Load the datasets using your absolute paths
df_petrol = pd.read_csv('/content/country_petrol_2020_2026.csv')
df_crude = pd.read_csv('/content/crude_oil_2020_2026.csv')

from google.colab import drive
drive.mount('/content/drive')

# 3. Data Transformation (Aggregating)
# We calculate the average global crude price for each month
df_crude_monthly = df_crude.groupby(['Year', 'Month']).agg({
    'Brent_USD': 'mean'
}).reset_index().rename(columns={'Brent_USD': 'Global_Brent_Avg'})

# 4. Data Merging
# Combine the national petrol prices with global crude benchmarks
df_final = pd.merge(df_petrol, df_crude_monthly, on=['Year', 'Month'], how='left')

# 5. Feature Engineering (New Columns)
# Difference between retail petrol and global raw material cost
df_final['Price_Gap'] = df_final['Petrol_USD'] - (df_final['Global_Brent_Avg'] / 159)

# Calculate Month-over-Month change in petrol price for each country
df_final = df_final.sort_values(by=['Country', 'Date'])
df_final['MoM_Change'] = df_final.groupby('Country')['Petrol_USD'].diff()

print(" STEP 1 SUCCESSFUL!")
print(f"Total records processed: {len(df_final)}")
df_final.head()

"""# **Clean the dataset**:"""

# 1. Fill NaN in MoM_Change with 0 (assuming no change for the first month)
df_final['MoM_Change'] = df_final['MoM_Change'].fillna(0)

# 2. Fill any missing Global Averages with the overall average (if any)
df_final['Global_Brent_Avg'] = df_final['Global_Brent_Avg'].fillna(df_final['Global_Brent_Avg'].mean())

# 3. Final Check
print("Checking for remaining NaNs:")
print(df_final.isnull().sum())

# Show the first few rows - January should now show 0.0 instead of NaN
df_final.head()

