fig1.show()

"""Decision-Making Insight: If a policymaker sees a country’s price suddenly spike while the global average (Brent) is stable, they can investigate local supply chain issues or tax changes.

# **2: Global Vulnerability Map (Geospatial)**

It provides an immediate geographical context. We are mapping "Petrol Price" to see which regions are "Hotspots" for energy unaffordability
"""

# Aggregate average price per country for a clean map
map_data = df_final.groupby(['Country', 'ISO', 'Region'])['Petrol_USD'].mean().reset_index()

fig2 = px.choropleth(map_data,
                    locations="ISO",
                    color="Petrol_USD",
                    hover_name="Country",
                    projection="natural earth",
                    color_continuous_scale=px.colors.sequential.Plasma,
                    title="Advanced Technique 2: Geospatial Distribution of Fuel Costs",
                    labels={'Petrol_USD':'Avg Price (USD)'})

fig2.update_layout(margin={"r":0,"t":50,"l":0,"b":0})
fig2.show()

"""High-cost "purple" zones on the map indicate regions where the government might need to implement price ceilings to meet SDG 7 targets for affordable energy.

# **3: Faceted Regional Comparison (Small Multiples)**

"Faceting" splits the chart into multiple sub-plots. It allows us to compare the "Price Gap" (the difference between global crude and local petrol) across different continents side-by-side.
"""

# Faceted Area Chart
fig3 = px.area(df_final,
               x="Date",
               y="Price_Gap",
               color="Region",
               facet_col="Region",
               facet_col_wrap=3,
               title="Advanced Technique 3: Regional 'Price Gap' Analysis (Retail vs Crude)",
               labels={"Price_Gap": "Price Difference (USD)"})

fig3.update_layout(showlegend=False)
fig3.show()

